function test_approx_R
    R = [1.000000000000000  -0.275095847318244   0.218350663146334; ...
         0.289629477625516   1.000000000000000  -0.036957013524625; ...
        -0.198669330795061   0.097843395007256   1.000000000000000];

    % Assert
    assert(all(all(abs(alg.approx_R(R) - [0.942081218933479  -0.262039943404723   0.209327602081667;
                                          0.275536895180392   0.960554148291037  -0.037618447538811;
                                         -0.191212960667317   0.093117110464887   0.977121695292612]) < 1e-4)));

    % Also check for an invalid rotation
    R = [0.903720560556316   0.698745832334794   0.744074260367462; ...
         0.890922504330789   0.197809826685929   0.500022435590201; ...
         0.334163052737496   0.030540946304637   0.479922141146060];

    assert(all(all(isnan(alg.approx_R(R)))));

    % Also check for nan
    R = nan(3);
    assert(all(all(isnan(alg.approx_R(R)))));
end
