function test_array_gauss
    array = [0 0 0;
             0 1 0;
             0 0 0];

    assert(all(all(abs(alg.array_gauss(array,1) - [0.058581536330607   0.096584625018564   0.058581536330607;
                                                   0.096584625018564   0.159241125690702   0.096584625018564;
                                                   0.058581536330607   0.096584625018564   0.058581536330607]) < eps('single'))))
end