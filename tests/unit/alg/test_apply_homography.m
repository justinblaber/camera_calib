function test_apply_homography
    points_1 = [0                   0;
                0                   42.671999999999997;
                42.671999999999997  0;
                42.671999999999997  42.671999999999997];

    points_2 = 1.0e+03 * [1.464595380250114   1.332481167835518;
                          1.728575470995986   0.592846556185128;
                          0.758142931060929   0.988299179810777;
                          1.089910536246923   0.197818029976501];

    homography_1_2 = 1.0e+03 * [-0.018210854110471   0.008525092901192   1.464595380250114;
                                -0.010223759443507  -0.016530874118072   1.332481167835518;
                                -0.000002183551664   0.000001353040608   0.001000000000000];

    assert(all(all(abs(alg.apply_homography(homography_1_2,points_1) - points_2) < eps('single'))));
end