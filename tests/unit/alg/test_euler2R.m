function test_euler2R
    euler = [0.1; 0.2; 0.3];
    R = alg.euler2R(euler);

    % Assert
    assert(all(all(abs(R - [0.936293363584199  -0.275095847318244   0.218350663146334; ...
                            0.289629477625516   0.956425085849232  -0.036957013524625; ...
                           -0.198669330795061   0.097843395007256   0.975170327201816]) < 1e-4)));
    assert(all(abs(alg.R2euler(R)-euler) < 1e-4));
end
