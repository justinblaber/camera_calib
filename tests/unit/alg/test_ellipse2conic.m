function test_ellipse2conic
    e = [23.251025401256015;
         18.606973065070964;
          7.649815052476453;
          6.380726808930452;
          2.133246917273027];

    Aq = alg.ellipse2conic(e);

    % Assert
    assert(all(all(abs(Aq./Aq(end) - [ 0.001083962192368   0.000162879686933  -0.028233930416376;
                                       0.000162879686933   0.000928248440836  -0.021059013474557;
                                      -0.028233930416376  -0.021059013474557   1.000000000000000]) < 1e-4)));
end
