function test_init_extrinsic_params
    H12 = 1.0e+02 * [ 0.001688622580971   0.005970472474763  -0.298997629664358;
                     -0.004643111492241   0.000031860634405   5.221183861905018;
                      0.000004229440713  -0.000000589902232   0.010000000000000];
               
    % Assert
    [R, t] = alg.init_extrinsic_params(H12, 1.0e+02 * [5.800179257855051 0                 3.205000000000000;
                                                       0                 5.800179257855051 2.405000000000000;
                                                       0                 0                 0.010000000000000]);
    
    assert(all(all(abs(R - [ 0.050919203994490   0.997916660938668   0.039617830399779
